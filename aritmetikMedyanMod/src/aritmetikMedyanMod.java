    private void hesaplaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hesaplaActionPerformed
        sonuclist.setText("");
        float ort;
        float sum = 0;
        for (int i = 0; i < list.size(); i++) {
            sum += list.get(i);
            sonuclist.append(list.get(i) + " ");
        }
        ort = sum / list.size();
        String gecici = new DecimalFormat("##.##").format(ort);
        artort.setText(gecici);
        /**
         * ****************************************************
         */
        float medyanDegeri;
        ArrayList<Integer> medyanYedek = list;
        Collections.sort(medyanYedek);
        if (medyanYedek.size() % 2 == 1) {
            medyanDegeri = (int) medyanYedek.get((medyanYedek.size()) / 2);
        } else {
            medyanDegeri = (medyanYedek.get(medyanYedek.size() / 2 - 1) + medyanYedek.get((medyanYedek.size() / 2))) / 2;
        }
        medyan.setText(medyanDegeri + "");
        /**
         * ****************************************************
         */
        ArrayList<Integer> frekansList = new ArrayList<Integer>();
        ArrayList<Integer> yedekList = list;
        int kac = 0;

        for (int i = 0; i < list.size(); i++) {
            for (int j = 0; j < list.size(); j++) {
                if (Objects.equals(list.get(i), list.get(j)) && i != j) {
                    kac++;
                    yedekList.remove(j);
                    yedekList.add(j, null);
                }
            }
            if (list.get(i) != null) {
                frekansList.add(kac + 1);
            }
            kac = 0;
        }
        // frekanslar iÃ§inde en fazla olan

        int max = frekansList.get(0);

        for (int i = 1; i < frekansList.size(); i++) {
            if (max < frekansList.get(i)) {
                max = frekansList.get(i);
                index = i;
            }
        }
        
        int sira = 0;
        for (int i = 0; i < list.size(); i++) {
            if (yedekList.get(i) != null) {
                sira++;
                if (sira == index + 1) {
                    System.out.println(sira);
                    mod.setText(String.valueOf(yedekList.get(i)));
                }
            }
        }


    }//GEN-LAST:event_hesaplaActionPerformed

